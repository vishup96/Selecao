const buttons = document.querySelectorAll('.slider-button');

buttons.forEach(button => {
  const text = button.querySelector('.slider-button-text');

  button.addEventListener('mousemove', (e) => {
    const rect = button.getBoundingClientRect();
    const x = e.clientX - rect.left;
    const y = e.clientY - rect.top;

    const centerX = rect.width / 2;
    const centerY = rect.height / 2;

    const moveX = (x - centerX) / 6;
    const moveY = (y - centerY) / 6;

    button.style.transform = `translate3d(${moveX}px, ${moveY}px, 0)`;
    text.style.transform = `translate3d(${-moveX}px, ${-moveY}px, 0)`;
  });

  button.addEventListener('mouseleave', () => {
    button.style.transform = `translate3d(0, 0, 0)`;
    text.style.transform = `translate3d(0, 0, 0)`;
  });
});


